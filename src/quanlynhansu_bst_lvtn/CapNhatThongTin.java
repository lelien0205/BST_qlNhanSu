    private void loadData() {
        jTextField_maNhanVien.setText(maNhanVien);
        try {
            oldNhanVien = getNhanVien(maNhanVien);
        } catch (Exception e) {
        }
        jTextField_hoTen.setText(oldNhanVien.getHoTen());
        if (oldNhanVien.getGioiTinh().equalsIgnoreCase("Nam")) {
            jRadioButton_nam.setSelected(true);
        } else {
            jRadioButton_nu.setSelected(true);
        }
        jDateChooser_ngaySinh.setDate(oldNhanVien.getNgaySinh());
        jTextArea_diaChi.setText(oldNhanVien.getDiaChi());
        jTextField_soDienThoai.setText(oldNhanVien.getSoDienThoai());
        jTextField_queQuan.setText(oldNhanVien.getQueQuan());
        jComboBox_phong.setSelectedItem(oldNhanVien.getMaPhong());
        jComboBox_chucVu.setSelectedItem(oldNhanVien.getMaChucVu());
        jTextField_matKhau.setText(oldNhanVien.getMatKhau());
        if (oldNhanVien.isLoaiNguoiDung()) {
            jRadioButton_quanLy.setSelected(true);
        } else {
            jRadioButton_nhanVien.setSelected(true);
        }
        jTextField_heSoLuong.setText(Double.toString(oldNhanVien.getHeSoLuong()));
    }

    private NhanVien getNhanVien(String maNhanVien) throws SQLException {
        NhanVien nhanvien = null;
        ResultSet nhanVienResultSet = GenericClass.getResultSet(connection, "select * from nhanvien where MaNhanVien = '" + maNhanVien + "'");
        while (nhanVienResultSet.next()) {
            String hoTen = nhanVienResultSet.getString("HoTen");
            String gioiTinh = nhanVienResultSet.getString("GioiTinh");
            java.sql.Date ngaySinh = nhanVienResultSet.getDate("NgaySinh");
            String diaChi = nhanVienResultSet.getString("DiaChi");
            String soDienThoai = nhanVienResultSet.getString("SoDienThoai");
            String queQuan = nhanVienResultSet.getString("QueQuan");
            String maPhong = nhanVienResultSet.getString("MaPhong");
            String maChucVu = nhanVienResultSet.getString("MaChucVu");
            String matKhau = nhanVienResultSet.getString("MatKhau");
            boolean loaiNguoiDung = nhanVienResultSet.getBoolean("LoaiNguoiDung");
            double heSoLuong = nhanVienResultSet.getDouble("HeSoLuong");
            nhanvien = new NhanVien(maNhanVien, hoTen, gioiTinh, ngaySinh, diaChi, soDienThoai, queQuan, maPhong, maChucVu, matKhau, loaiNguoiDung, heSoLuong);
        }
        return nhanvien;
    }

