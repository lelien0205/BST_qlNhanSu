    private void jButton_capNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_capNhatActionPerformed
        // TODO add your handling code here:
        if (jTextField_hoTen.getText().isEmpty() || jTextField_queQuan.getText().isEmpty() || jTextField_heSoLuong.getText().isEmpty() || jTextField_soDienThoai.getText().isEmpty() || jTextArea_diaChi.getText().isEmpty() || jTextField_matKhau.getText().isEmpty() || jDateChooser_ngaySinh.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ dữ liệu\nVui lòng kiểm tra lại", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            String _maNhanVien = jTextField_maNhanVien.getText();
            String hoTen = jTextField_hoTen.getText();
            int gioiTinh = jRadioButton_nam.isSelected() ? 1 : 0;
            Date ngaySinh = GenericClass.getDateFromDateChooser(jDateChooser_ngaySinh);
            String diaChi = jTextArea_diaChi.getText();
            String soDienThoai = jTextField_soDienThoai.getText();
            String queQuan = jTextField_queQuan.getText();
            String maPhong = jComboBox_phong.getSelectedItem().toString();
            String maChucVu = jComboBox_chucVu.getSelectedItem().toString();
            String matKhau = jTextField_matKhau.getText();
            boolean loaiNguoiDung = jRadioButton_quanLy.isSelected() ? true : false;
            double heSoLuong = Double.parseDouble(jTextField_heSoLuong.getText());
            String gioiTinhString = gioiTinh == 1 ? "Nam" : "Nữ";
            int loaiNguoiDungBit = loaiNguoiDung ? 1 : 0;
            LocalDate ngaySinhDate = LocalDate.parse(GenericClass.dateConverter(ngaySinh));
            if (!GenericClass.checkAge(ngaySinhDate)) {
                JOptionPane.showMessageDialog(this, "Nhân viên phải > 18 tuổi\nVui lòng kiểm tra lại", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                int luaChon = JOptionPane.showConfirmDialog(
                        this,
                        "Xác nhận lưu thay đổi?",
                        "Xác nhận",
                        JOptionPane.YES_NO_OPTION);

                // Kiểm tra xem người dùng đã chọn Yes hay No
                if (luaChon == JOptionPane.YES_OPTION) {
                    MyTrees.nhanVienTree.modifyData(oldNhanVien, new NhanVien(_maNhanVien, hoTen, gioiTinhString, ngaySinh, diaChi, soDienThoai, queQuan, maPhong, maChucVu, matKhau, loaiNguoiDung, heSoLuong));
                    GenericClass.modifyDB(connection, "update nhanvien set HoTen = '" + hoTen + "', GioiTinh = '" + gioiTinhString + "', NgaySinh = '" + GenericClass.dateConverter(ngaySinh) + "', DiaChi = '" + diaChi + "', SoDienThoai = '" + soDienThoai + "', QueQuan = '" + queQuan + "', MatKhau = '" + matKhau + "', LoaiNguoiDung = b'" + loaiNguoiDungBit + "', HeSoLuong = '" + heSoLuong + "' where MaNhanVien = '" + _maNhanVien + "'", this, "Cập nhật dữ liệu thành công!");
                }
            }
        }
        this.dispose();
    }//GEN-LAST:event_jButton_capNhatActionPerformed

