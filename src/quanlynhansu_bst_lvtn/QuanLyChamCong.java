    private double tinhLuong(double heSoLuong, int soNgayLamViec, double heSoPhuCap) {
        double tienPhat = 0.0;
        if (soNgayLamViec < 20) {
            tienPhat = (20 - soNgayLamViec) * 500;
        }
        return (heSoLuong * 1800000 + soNgayLamViec * 200000 * heSoPhuCap) - tienPhat;
    }

    private void loadData(JTable jTable) {
        jComboBox_thang.setSelectedItem(LocalDate.now().getMonthValue());
        jComboBox_nam.setSelectedItem(LocalDate.now().getYear());
        GenericClass.addObjectsToTable(jTable, 1, SortOrder.ASCENDING);
        GenericClass.getTreeData(MyTrees.nhanVienTree.root, (DefaultTableModel) jTable_dsNhanVien.getModel(), (NhanVien) -> new Object[]{NhanVien.getMaNhanVien(), NhanVien.getHoTen(), NhanVien.getNgaySinh(), NhanVien.getMaPhong()});
    }

