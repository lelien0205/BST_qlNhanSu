    private void insertIntoLuong(String maLuong, String maNhanVien, int thang, int nam, double luong) {
        String query = "insert into Luong values('" + maLuong + "', '" + maNhanVien + "'," + thang + ", " + nam + ", " + luong + " )";
        MyTrees.luongTree.insert(new Luong(maLuong, maNhanVien, thang, nam, luong));
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            statement.executeUpdate();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra\nVui lòng thử lại", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void jComboBox_thaoTacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_thaoTacActionPerformed
        // TODO add your handling code here:
        LocalDate currentDate = LocalDate.now();
        int currentYear = currentDate.getYear();
        int currentMonth = currentDate.getMonthValue();
        if (jComboBox_thaoTac.getSelectedIndex() == 0) {
            if (jTextField_soNgayLamViec.getText().isEmpty() || jTextField_maNhanVien.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Chưa nhập đủ dữ liệu\nVui lòng kiểm tra lại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            } else {
                int thang = Integer.parseInt(jComboBox_thang.getSelectedItem().toString());
                int nam = Integer.parseInt(jComboBox_nam.getSelectedItem().toString());

                // Kiểm tra năm nhuận
                boolean isLeapYear = GenericClass.isLeapYear(nam);

                if ((thang == 2 && isLeapYear && Integer.parseInt(jTextField_soNgayLamViec.getText()) > 29)
                        || (thang == 2 && !isLeapYear && Integer.parseInt(jTextField_soNgayLamViec.getText()) > 28)
                        || ((thang == 4 || thang == 6 || thang == 9 || thang == 11) && Integer.parseInt(jTextField_soNgayLamViec.getText()) > 30)
                        || (Integer.parseInt(jTextField_soNgayLamViec.getText()) > 31)) {
                    JOptionPane.showMessageDialog(this, "Số ngày làm việc không hợp lệ cho tháng và năm đã chọn", "Thông báo", JOptionPane.ERROR_MESSAGE);
                } else {
                    if (nam > currentYear || (nam == currentYear && thang > currentMonth)) {
                        JOptionPane.showMessageDialog(this, "Thời gian không được quá thời gian hiện tại", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } else {
                        String maChamCong = GenericClass.generateRandomString("CC", 10000, 90000);
                        String maNhanVien = jTextField_maNhanVien.getText();
                        int soNgayLamViec = Integer.parseInt(jTextField_soNgayLamViec.getText());
                        long duplicateCheck = (long) GenericClass.getParameter(GenericClass.getResultSet(connection, "select count(*) from ChamCong where MaNhanVien = '" + maNhanVien + "' and Thang =" + thang + " and Nam = " + nam));
                        if (duplicateCheck > 0) {
                            JOptionPane.showMessageDialog(this, "Chấm công tháng " + thang + "/" + nam + " của nhân viên " + (String) GenericClass.getParameter(GenericClass.getResultSet(connection, "select HoTen from NhanVien where MaNhanVien = '" + maNhanVien + "'")) + " đã tồn tại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            MyTrees.chamCongTree.insert(new ChamCong(maChamCong, maNhanVien, thang, nam, soNgayLamViec));
                            GenericClass.modifyDB(connection, "insert into ChamCong values('" + maChamCong + "', '" + maNhanVien + "'," + thang + " ," + nam + " ," + soNgayLamViec + ")", this, "Thêm dữ liệu thành công!");

                            BigDecimal heSoPhuCap = (BigDecimal) GenericClass.getParameter(GenericClass.getResultSet(connection, "select cv.HeSoPhuCap from NhanVien nv join ChucVu cv on nv.MaChucVu = cv.MaChucVu where nv.MaNhanVien = '" + maNhanVien + "'"));
                            BigDecimal heSoLuong = (BigDecimal) GenericClass.getParameter(GenericClass.getResultSet(connection, "select HeSoLuong from NhanVien where MaNhanVien = '" + maNhanVien + "'"));
                            insertIntoLuong(GenericClass.generateRandomString("L", 10000, 90000), maNhanVien, thang, nam, tinhLuong(heSoLuong.doubleValue(), soNgayLamViec, heSoPhuCap.doubleValue()));
                        }
                    }
                }
                resetInput();
            }
        } else {
            GenericClass.isQuanLy = true;
            BangChamCong bangChamCong = new BangChamCong();
            BangChamCong.maNhanVien = jTextField_maNhanVien.getText();
            this.setVisible(false);
            bangChamCong.setVisible(true);
            resetInput();
        }
    }//GEN-LAST:event_jComboBox_thaoTacActionPerformed

