    private Luong getLuong(String maNhanVien, int thang, int nam) throws SQLException {
        Luong luongObj = null;
        ResultSet luongResultSet = GenericClass.getResultSet(connection, "select * from luong where MaNhanVien = '" + maNhanVien + "' and Thang = " + thang + " and Nam = " + nam + "");
        while (luongResultSet.next()) {
            String maLuong = luongResultSet.getString("MaLuong");
            String _maNhanVien = luongResultSet.getString("MaNhanVien");
            int _thang = luongResultSet.getInt("Thang");
            int _nam = luongResultSet.getInt("Nam");
            double luong = luongResultSet.getDouble("Luong");
            luongObj = new Luong(maLuong, _maNhanVien, _thang, _nam, luong);
        }
        return luongObj;
    }

