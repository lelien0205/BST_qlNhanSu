    private void loadData(JTable jTable) {
        jComboBox_thangLoc.setSelectedItem(LocalDate.now().getMonthValue());
        jComboBox_namLoc.setSelectedItem(LocalDate.now().getYear());
        GenericClass.addObjectsToTable(jTable, 4, SortOrder.ASCENDING);
        GenericClass.getTreeData(MyTrees.luongTree.root, (DefaultTableModel) jTable.getModel(), (Luong) -> new Object[]{Luong.getMaLuong(), Luong.getMaNhanVien(), 0, Luong.getThang(), Luong.getNam(), (int) Luong.getLuong()});
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String maPhong = (String) GenericClass.getParameter(GenericClass.getResultSet(connection, "select MaPhong from NhanVien where MaNhanVien = '" + model.getValueAt(i, 1).toString() + "'"));
            model.setValueAt(maPhong, i, 6);
        }
        for (int i = 0; i < model.getRowCount(); i++) {
            String hoTen = (String) GenericClass.getParameter(GenericClass.getResultSet(connection, "select Hoten from NhanVien where MaNhanVien = '" + model.getValueAt(i, 1).toString() + "'"));
            model.setValueAt(hoTen, i, 2);
        }
//        GenericClass.tableFilter(jTable_dsLuong, 2, jComboBox_phong.getSelectedItem().toString(), SortOrder.ASCENDING);
        tableFilter(jTable, 2, jComboBox_phong.getSelectedItem().toString(), Integer.parseInt(jComboBox_thangLoc.getSelectedItem().toString()), Integer.parseInt(jComboBox_namLoc.getSelectedItem().toString()), SortOrder.ASCENDING);
        GenericClass.tinhThue(jTable, 5);
    }

    public static void tableFilter(JTable jTable, int columnIndex, String regex, int thang, int nam, SortOrder sortOrder) {
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        jTable.setRowSorter(sorter);

        RowFilter<Object, Object> regexRowFilter = null;
        try {
            regexRowFilter = RowFilter.regexFilter("\\b" + regex + "\\b");
        } catch (PatternSyntaxException e) {
            // Invalid regex
            return;
        }

        RowFilter<Object, Object> thangNamRowFilter = new RowFilter<Object, Object>() {
            public boolean include(Entry<? extends Object, ? extends Object> entry) {
                int entryThang = (Integer) entry.getValue(3);
                int entryNam = (Integer) entry.getValue(4);
                return entryThang == thang && entryNam == nam;
            }
        };

        // Combine existing regex filter and thangNam filter with an AND condition
        RowFilter<Object, Object> combinedRowFilter = RowFilter.andFilter(Arrays.asList(regexRowFilter, thangNamRowFilter));

        sorter.setRowFilter(combinedRowFilter);
        sorter.setSortKeys(Arrays.asList(new RowSorter.SortKey(columnIndex, sortOrder)));
    }

